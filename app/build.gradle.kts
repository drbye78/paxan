tasks.register<Copy>("copyToDist") {
    dependsOn("assembleDebug")
    val buildDir = layout.buildDirectory.get().asFile
    val apkFile = buildDir.resolve("outputs/apk/debug/app-debug.apk")
    val distDir = rootProject.file("dist")
    
    from(apkFile)
    into(distDir)
    rename { "proxymania-android-debug.apk" }
    
    doFirst {
        if (!distDir.exists()) {
            distDir.mkdirs()
        }
    }
ksp {
    arg("dagger.fastInitialization", "true")
}

