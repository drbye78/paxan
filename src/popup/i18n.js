let currentLang = 'ru';

function setLanguage(lang) {
  if (translations[lang]) {
    currentLang = lang;
    applyTranslations();
  }
}

function t(key) {
  return translations[currentLang][key] || translations['en'][key] || key;
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[currentLang][key]) {
      el.textContent = translations[currentLang][key];
    }
  });
  
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (translations[currentLang][key]) {
      el.placeholder = translations[currentLang][key];
    }
  });
  
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    if (translations[currentLang][key]) {
      el.title = translations[currentLang][key];
    }
  });
  
  updateDynamicText();
}

function updateDynamicText() {
  const warningText = document.querySelector('.security-warning .warning-text');
  if (warningText) warningText.innerHTML = t('security_warning');
  
  const statusText = document.querySelector('.status-text');
  if (statusText && !document.querySelector('.status-badge')?.classList.contains('connected')) {
    statusText.textContent = t('disconnected');
  }
  
  const listTitle = document.getElementById('listTitle');
  if (listTitle) listTitle.textContent = t('available_proxies');
  
  const loadingText = document.querySelector('.loading-text');
  if (loadingText) loadingText.textContent = t('loading_proxies');
  
  const loadingHint = document.querySelector('.loading-hint');
  if (loadingHint) loadingHint.textContent = t('loading_hint');
  
  const searchInput = document.getElementById('proxySearch');
  if (searchInput) searchInput.placeholder = t('search_placeholder');
  
  const emptyTitle = document.querySelector('.empty-state h4');
  if (emptyTitle) emptyTitle.textContent = t('no_proxies_found');
  
  const emptyText = document.querySelector('.empty-state p');
  if (emptyText) emptyText.textContent = t('try_adjusting_filters');
  
  const emptyRefreshBtn = document.getElementById('emptyRefreshBtn');
  if (emptyRefreshBtn) emptyRefreshBtn.textContent = t('refresh');
  
  const settingsTitle = document.querySelector('#settingsPanel h2');
  if (settingsTitle) settingsTitle.textContent = t('settings');
  
  const statsTitle = document.querySelector('#statsPanel h2');
  if (statsTitle) statsTitle.textContent = t('statistics');
  
  const detailsToggle = document.getElementById('detailsToggle');
  if (detailsToggle) detailsToggle.querySelector('span').textContent = t('details');
  
  const ipDetectorHeader = document.querySelector('.ip-detector-header span');
  if (ipDetectorHeader) ipDetectorHeader.textContent = t('ip_address_check');
  
  const checkIpBtn = document.getElementById('ipCheckBtn');
  if (checkIpBtn) checkIpBtn.textContent = t('check_ips');
  
  const quickConnectTitle = document.querySelector('#quickConnectSection .section-title');
  if (quickConnectTitle) quickConnectTitle.textContent = t('quick_connect');
  
  const settingsHeaders = document.querySelectorAll('#settingsPanel .setting-group h3');
  if (settingsHeaders[0]) settingsHeaders[0].textContent = t('appearance');
  if (settingsHeaders[1]) settingsHeaders[1].textContent = t('connection');
  if (settingsHeaders[2]) settingsHeaders[2].textContent = t('notifications');
  if (settingsHeaders[3]) settingsHeaders[3].textContent = t('security');
  if (settingsHeaders[4]) settingsHeaders[4].textContent = t('advanced_features');
  if (settingsHeaders[5]) settingsHeaders[5].textContent = t('data');
  
  updateLabels();
}

function updateLabels() {
  const labels = {
    'themeSelect': 'theme',
    'languageSelect': 'language',
    'refreshInterval': 'auto_refresh',
    'proxySource': 'proxy_source',
    'rotationInterval': 'rotation_interval',
    'autoConnectToggle': 'auto_connect_startup',
    'autoFailoverToggle': 'auto_failover',
    'testBeforeConnectToggle': 'test_before_connect',
    'notificationsToggle': 'enable_notifications',
    'dnsLeakToggle': 'dns_leak_protection',
    'webRtcToggle': 'webrtc_protection',
    'rotationToggle': 'auto_rotation',
    'manageBlacklistBtn': 'country_blacklist',
    'addSiteRuleBtn': 'per_site_proxy_rules',
    'importBtn': 'import',
    'exportBtn': 'export',
    'clearDataBtn': 'clear_all'
  };
  
  Object.entries(labels).forEach(([id, key]) => {
    const el = document.getElementById(id)?.closest('.setting')?.querySelector('.setting-label');
    if (el) el.textContent = t(key);
  });
  
  const themeSelect = document.getElementById('themeSelect');
  if (themeSelect) {
    themeSelect.options[0].textContent = t('dark');
    themeSelect.options[1].textContent = t('light');
    themeSelect.options[2].textContent = t('system');
  }
  
  const languageSelect = document.getElementById('languageSelect');
  if (languageSelect) {
    languageSelect.options[0].textContent = 'Русский';
    languageSelect.options[1].textContent = 'English';
  }
}

